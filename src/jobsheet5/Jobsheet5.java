public class Jobsheet5 {
    public static void main(String[] args) {
        int a = 10;
        int b = 2;
        
        int c;
        
        //Penjumlahan
        c = a + b;
        System.out.println(a + " + " + b + " = " + c);
        
        //Pengurangan
        c = a - b;
        System.out.println(a + " - " + b + " = " + c);
        
        //Perkalian
        c = a * b;
        System.out.println(a + " * " + b + " = " + c);
        
        //Pembagian
        c = a / b;
        System.out.println(a + " / " + b + " = " + c);
        
        //Sisa Pembagian
        c = a % b;
        System.out.println(a + " % " + b + " = " + c);
        
    }
    
}
